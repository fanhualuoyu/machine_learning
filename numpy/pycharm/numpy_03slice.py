import numpy as np

t1 = np.arange(100).reshape((25, 4))

# 取行
print("取t1的第三行:", t1[2])

# 取连续的多行
print("连续的多行:", t1[2:])

# 取不连续的多行
print("不连续的多行:", t1[[2, 6, 10]])

# 取列
print("取列:", t1[:, 0])

# 取连续的多列
print("取连续的多列:", t1[:, 2:])

# 取不连续的多列
print("不连续的多列:", t1[:, [0, 2]])

# 取行和列
print("第三行，第四列的值:", t1[2, 3])

# 取多行多列
print("取第三行到第五行，第二列到第四列:", t1[2:5, 1:4])

# 取多个不相邻的行和列
print("取不相邻的多个点:", t1[[0, 2], [0, 1]])  # (0,0),(2,1)
